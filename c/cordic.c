real_t compute_K(const uint32_t N)
{
#if defined(COMPUTE_K)
	real_t K = 1;
	int i;

	for (i = 0; i < N; i++) {
		K = K * cos(atan_powers_of_two[i]);
	}
	return K;
#else
	(void) N;
	/* K computed up to N = 64 */
	return (0.60725293500888144482274810798116959631443023681640625);
#endif
}


void cordic(
		const real_t angle,
		real_t * sine,
		real_t * cosine)
{
	real_t c = compute_K(CORDIC_LUT_LENGTH), s = 0.0;
	real_t c_new = 0.0, s_new = 0.0;
	real_t phi = angle;
	uint32_t i = 0;
	real_t direction = 0.0;

	for (i = 0; i < CORDIC_LUT_LENGTH; i++) {
		direction = (phi > 0.0 ? 1.0 : -1.0);
		c_new = c - direction * (s * powers_of_two[i]);
		s_new = s + direction * (c * powers_of_two[i]);
		c = c_new;
		s = s_new;
		phi = phi - (direction * atan_powers_of_two[i]);
	}
	*sine = s;
	*cosine = c;
}
